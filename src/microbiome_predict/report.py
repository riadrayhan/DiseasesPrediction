def _esc(value) -> str:
    return html.escape(str(value))


def _prob_bar(p: float, for_pdf: bool = False) -> str:
    pct = max(0.0, min(1.0, float(p))) * 100
    if for_pdf:
        return f"{pct:.1f}%"
    return f'<div class="bar"><span style="width:{pct:.1f}%"></span></div>'


def _badge(label: str, indeterminate: bool) -> str:
    if indeterminate:
        return f'<span class="badge warn">{_esc(label)}</span>'
    cls = "ok" if str(label).lower() in {"healthy", "control", "0"} else "bad"
    return f'<span class="badge {cls}">{_esc(label)}</span>'


def _render_sample(s: SamplePrediction, reject_threshold: float,
                   for_pdf: bool = False) -> str:
    rows = []
    for cls, p in sorted(s.probabilities.items(), key=lambda kv: -kv[1]):
        lo, hi = s.proba_interval.get(cls, (p, p))
        rows.append(
            f"<tr><td>{_esc(cls)}</td>"
            f"<td>{p:.1%}</td>"
            f"<td class='muted'>[{lo:.1%}, {hi:.1%}]</td>"
            f"<td style='width:30%'>{_prob_bar(p, for_pdf)}</td></tr>"
        )
    prob_table = (
        "<table><tr><th>Condition</th><th>Probability</th>"
        "<th>95% interval</th><th></th></tr>" + "".join(rows) + "</table>"
    )

    # Confidence / reject status
    if s.indeterminate:
        conf_note = (
            f"<p class='section-note'>Confidence {s.confidence:.1%} is below the "
            f"{reject_threshold:.0%} reporting threshold &rarr; result withheld as "
            f"<strong>INDETERMINATE</strong> (reject option).</p>"
        )
    else:
        conf_note = (
            f"<p class='section-note'>Top-class confidence: "
            f"<strong>{s.confidence:.1%}</strong> "
            f"(meets the {reject_threshold:.0%} threshold).</p>"
        )

    # Wellness
    wellness_html = ""
    if s.wellness_index is not None:
        wellness_html = (
            "<h3>Disease Risk Analysis — Wellness Index (GMWI2-style)</h3>"
            f"<p class='section-note'>Index score "
            f"<strong>{s.wellness_index:+.3f}</strong> "
            f"(positive = healthier profile) &rarr; {_badge(s.wellness_call or '—', False)}"
            "</p>"
        )

    # Future forecast
    if s.future_risk:
        fr_rows = "".join(
            f"<tr><td>{_esc(k)}</td><td>{v:.3g}</td></tr>"
            for k, v in s.future_risk.items()
        )
        future_html = (
            "<h3>Future Disease Forecast</h3>"
            "<table><tr><th>Outcome</th><th>Relative risk / score</th></tr>"
            f"{fr_rows}</table>"
        )
    else:
        future_html = (
            "<h3>Future Disease Forecast</h3>"
            "<p class='section-note'>Not available — no longitudinal/prognostic "
            "model was supplied for this run.</p>"
        )

    # Interpretability
    if s.top_features:
        feat_rows = "".join(
            f"<tr><td>{_esc(name)}</td><td>{val:+.4f}</td>"
            f"<td class='muted'>{'raises' if val >= 0 else 'lowers'} predicted risk</td></tr>"
            for name, val in s.top_features
        )
        interpret_html = (
            "<h3>Interpretability &amp; Actionable Insights</h3>"
            "<p class='section-note'>Taxa with the largest influence on this "
            "sample's prediction (occlusion/SHAP contribution to the predicted "
            "class):</p>"
            "<table><tr><th>Feature (taxon/pathway)</th><th>Contribution</th>"
            f"<th></th></tr>{feat_rows}</table>"
        )
    else:
        interpret_html = ""

    return f"""
    <section class="card">
      <h2>Sample: {_esc(s.sample)}</h2>
      <h3>Current Disease Prediction</h3>
      <p>Predicted status: {_badge(s.predicted_label, s.indeterminate)}</p>
      {prob_table}
      <h3>Model Confidence &amp; Uncertainty</h3>
      {conf_note}
      {wellness_html}
      {future_html}
      {interpret_html}
    </section>
    """


def render_html_report(report: ReportData, for_pdf: bool = False) -> str:
    """Render the full report to a single self-contained HTML string.

    Set ``for_pdf=True`` to emit a stylesheet compatible with the xhtml2pdf
    renderer (see :mod:`microbiome_predict.pdf`).
    """
    css = _CSS_PDF if for_pdf else _CSS
    generated = datetime.now().strftime("%Y-%m-%d %H:%M:%S")

    qc_html = ""
    if report.qc_summary is not None and not report.qc_summary.empty:
        qc_html = (
            '<section class="card"><h2>Sample &amp; Data Quality Summary</h2>'
            '<p class="section-note">Preprocessing read-tracking and '
            "classification rates per sample.</p>"
            + report.qc_summary.to_html(index=False, border=0)
            + "</section>"
        )

    model_rows = "".join(
        f"<tr><td>{_esc(k)}</td><td>{_esc(v)}</td></tr>"
        for k, v in report.model_info.items()
    )
    model_html = (
        '<section class="card"><h2>Model Information</h2>'
        f"<table>{model_rows}</table></section>"
        if model_rows
        else ""
    )

    samples_html = "".join(
        _render_sample(s, report.reject_threshold, for_pdf) for s in report.samples
    )

    return f"""<!doctype html>
<html lang="en"><head><meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>{_esc(report.title)}</title>
<style>{css}</style></head>
<body>
<header>
  <h1>{_esc(report.title)}</h1>
  <div class="meta">Generated {generated} &middot; {len(report.samples)} sample(s)
   &middot; reject threshold {report.reject_threshold:.0%}</div>
</header>
<main>
  <div class="disclaimer"><strong>Important:</strong> {_esc(report.disclaimer)}</div>
  {qc_html}
  {model_html}
  {samples_html}
</main>
<footer>
  microbiome-predict &middot; probabilistic decision-support output &middot;
  not a substitute for professional medical judgement.
</footer>
</body></html>
"""


def write_report(report: ReportData, path: str | Path) -> Path:
    """Render and write the HTML report, returning the output path."""
    out = Path(path)
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(render_html_report(report), encoding="utf-8")
    return out
