"""
Clinician-facing report generation (spec Section 4).

Renders a self-contained HTML report (no external assets) with the sections the
spec requires:

1. Sample & data-quality summary
2. Current disease prediction (probability + interval)
3. Disease risk analysis
4. Future-disease forecast (if a prognostic model was supplied)
5. Model confidence & uncertainty quantification
6. Interpretability & actionable insights (top contributing taxa)

A prominent, non-negotiable medical disclaimer is always included: this is
decision-support output, not a standalone diagnosis.
"""

from __future__ import annotations

import html
from dataclasses import dataclass, field
from datetime import datetime
from pathlib import Path
from typing import Dict, List, Optional, Tuple

import pandas as pd

DEFAULT_DISCLAIMER = (
    "This report is generated by research software for decision-support and "
    "investigational use. Microbiome-derived predictions are probabilistic "
    "inferences, not definitive diagnoses, and must be interpreted by a "
    "qualified clinician alongside other clinical findings. The only fully "
    "deterministic information here is the sequencing-derived taxonomic "
    "composition; all disease predictions carry the stated uncertainty."
)


@dataclass
class SamplePrediction:
    """All per-sample results needed to render one section of the report."""

    sample: str
    predicted_label: str
    probabilities: Dict[str, float]
    confidence: float
    proba_interval: Dict[str, Tuple[float, float]] = field(default_factory=dict)
    indeterminate: bool = False
    wellness_index: Optional[float] = None
    wellness_call: Optional[str] = None
    top_features: List[Tuple[str, float]] = field(default_factory=list)
    future_risk: Optional[Dict[str, float]] = None


@dataclass
class ReportData:
    samples: List[SamplePrediction]
    title: str = "Microbiome Disease-Prediction Report"
    qc_summary: Optional[pd.DataFrame] = None
    model_info: Dict[str, str] = field(default_factory=dict)
    reject_threshold: float = 0.9
    disclaimer: str = DEFAULT_DISCLAIMER


